#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new_str;
	int		i;

	i = 0;
	if (!s)
	{
		return (NULL);
	}
	new_str = malloc(ft_strlen(s) + 1);
	if (!new_str)
	{
		return (NULL);
	}
	while (s[i])
	{
		new_str[i] = f(i, s[i]);
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
